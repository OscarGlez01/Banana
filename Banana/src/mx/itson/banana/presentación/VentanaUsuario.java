    public void llenarTabla() {
        List<Usuario> usuarios = UsuarioDAO.obtenerTodos();
        DefaultTableModel mod = (DefaultTableModel) tblUsuario.getModel();
        
        for (Usuario usu : usuarios) {
            mod.addRow(new Object[]{
                usu.getId(),
                usu.getNickname(),
                usu.getEmail()});
        }
    }

